    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                     
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        String nik = model.getValueAt(selectedRow, 0).toString();
        String nama = model.getValueAt(selectedRow, 1).toString();
        String tglLahir = model.getValueAt(selectedRow, 2).toString();
        String jenisKelamin = model.getValueAt(selectedRow, 3).toString();

        jTextField1.setText(nik);
        jTextField2.setText(nama);
        jTextField3.setText(tglLahir);

        if (jenisKelamin.equals("Laki-laki")) {
            jRadioButton1.setSelected(true);
        } else {
            jRadioButton2.setSelected(true);
        }
    }
}

