    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
            String nik = jTextField1.getText();
            String nama = jTextField2.getText();
            String tglLahir = jTextField3.getText();

            String jenisKelamin = "";
            if (jRadioButton1.isSelected()) {
                jenisKelamin = "Laki-laki";
            } else if (jRadioButton2.isSelected()) {
                jenisKelamin = "Perempuan";
            }

            if (nik.isEmpty() || nama.isEmpty() || tglLahir.isEmpty() || jenisKelamin.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }


            for (int i = 0; i < model.getRowCount(); i++) {
                if (model.getValueAt(i, 0).equals(nik)) {
                    JOptionPane.showMessageDialog(this, "NIK " + nik + " sudah terdaftar!", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            Object[] data = {nik, nama, tglLahir, jenisKelamin};
            model.addRow(data);
            bersih_form();
    }//GEN-LAST:event_jToggleButton2ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diubah!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String nik = jTextField1.getText();
            String nama = jTextField2.getText();
            String tglLahir = jTextField3.getText();
            String jenisKelamin = jRadioButton1.isSelected() ? "Laki-laki" : "Perempuan";

            model.setValueAt(nik, selectedRow, 0);
            model.setValueAt(nama, selectedRow, 1);
            model.setValueAt(tglLahir, selectedRow, 2);
            model.setValueAt(jenisKelamin, selectedRow, 3);

            JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            bersih_form();
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void jToggleButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton4ActionPerformed

    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        model.removeRow(selectedRow);
        JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        bersih_form();
    }
    }//GEN-LAST:event_jToggleButton4ActionPerformed

