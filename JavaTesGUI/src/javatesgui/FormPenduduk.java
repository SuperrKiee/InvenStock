    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
            String nikCari = jTextField1.getText();
    if (nikCari.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan NIK yang ingin dicari!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    for (int i = 0; i < model.getRowCount(); i++) {
        if (model.getValueAt(i, 0).equals(nikCari)) {
            jTable1.setRowSelectionInterval(i, i);
            jTable1MouseClicked(null);
            return;
        }
    }

    JOptionPane.showMessageDialog(this, "Data dengan NIK " + nikCari + " tidak ditemukan.", "Info", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jToggleButton3ActionPerformed

